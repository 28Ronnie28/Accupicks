    public void EnableDisable(){
        if(PuntRadioBtn.isSelected()){
            MissedRadioBtn.setEnabled(false);
            OverRadioBtn.setEnabled(false);
        }else{
            if(RunRadioBtn.isSelected()){
                MissedRadioBtn.setEnabled(false);
                OverRadioBtn.setEnabled(false);
            }else{
                if(ScrumRadioBtn.isSelected()){
                    MissedRadioBtn.setEnabled(false);
                    OverRadioBtn.setEnabled(false);
                }else{
                    if(KickAtGoalRadioBtn.isSelected()){
                        MissedRadioBtn.setEnabled(true);
                        OverRadioBtn.setEnabled(true);
                    }
                }
            }
        }
    }

