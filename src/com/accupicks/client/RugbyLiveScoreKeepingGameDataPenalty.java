    private void CommitPenaltyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CommitPenaltyBtnActionPerformed
    String Penalty = PenaltyOffendedTxf.getText();
    
    if(!Penalty.equals("")){
        if(PuntRadioBtn.isSelected()){
            RugbyLiveScoreKeepingGameData.PenaltyOverOrMissed = "Punt$NA#"+Penalty;
        }else{
            if(RunRadioBtn.isSelected()){
                RugbyLiveScoreKeepingGameData.PenaltyOverOrMissed = "Run$NA#"+Penalty;
            }else{
                if(ScrumRadioBtn.isSelected()){
                    RugbyLiveScoreKeepingGameData.PenaltyOverOrMissed = "Scrum$NA#"+Penalty;
                }else{
                    if(KickAtGoalRadioBtn.isSelected()){
                        if(MissedRadioBtn.isSelected()){
                            RugbyLiveScoreKeepingGameData.PenaltyOverOrMissed = "KickAtGoal$Missed#"+Penalty;
                        }else{
                            RugbyLiveScoreKeepingGameData.PenaltyOverOrMissed = "KickAtGoal$Over#"+Penalty;
                        }
                    }
                }
            }
        }
    }else{
        JOptionPane.showMessageDialog(this, "Please Enter The Penalty What Was Commited.");
    }
        dispose();
    }//GEN-LAST:event_CommitPenaltyBtnActionPerformed

